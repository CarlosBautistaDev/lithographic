				A nombre de la Sociedad Mexicana de Radiología e Imagen les damos la más cordial bienvenida a nuestro LIII Curso Internacional de Radiología e Imagen y el XXXI Encuentro Internacional de Residentes y Radiólogos, el cual se llevará a cabo del 13 al 16 de Febrero 2019 en el Hotel Marquis Reforma en la Ciudad de México. <br><br>	

La Mesa Directiva y el Comité Organizador preparamos un programa académico muy interesante así como el ya conocido Curso de Administración CNMERI, Curso de Técnicos Radiólogos, el Concurso Diagnóstico Quiz, Trabajos Electrónicos y nuestro tradicional Encuentro Internacional de Residentes y Radiólogos.<br><br>

