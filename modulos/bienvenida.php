Este curso cuenta con la acreditación del Consejo Mexicano de Radiología e Imagen, quien nos ha otorgado 45 puntos para fines de certificación.<br>

Contamos con el área de exposición comercial, donde nos presentarán las novedades tecnológicas de nuestra especialidad. <br><br>

También la SMRI llevará a cabo el premio a la Excelencia Académica homenajeando a un distinguido Radiólogo en reconocimiento a su gran trayectoria.<br><br>

